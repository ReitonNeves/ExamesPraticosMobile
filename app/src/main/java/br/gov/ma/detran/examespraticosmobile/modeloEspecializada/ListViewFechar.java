package br.gov.ma.detran.examespraticosmobile.modeloEspecializada;

import br.gov.ma.detran.examespraticosmobile.modelo.AGC_Prova_Candidato;

public class ListViewFechar {
    private String nomeCandidato;
    private String resultado;
    private AGC_Prova_Candidato agcProvaCandidato;

    public ListViewFechar(){}

    public ListViewFechar(String nomeCandidato, AGC_Prova_Candidato agcProvaCandidato, String resultado) {
        this.nomeCandidato = nomeCandidato;
        this.agcProvaCandidato = agcProvaCandidato;
        this.resultado = resultado;
    }

    public String getNomeCandidato() {
        return nomeCandidato;
    }

    public void setNomeCandidato(String nomeCandidato) {
        this.nomeCandidato = nomeCandidato;
    }

    public AGC_Prova_Candidato getAgcProvaCandidato() {
        return agcProvaCandidato;
    }

    public void setAgcProvaCandidato(AGC_Prova_Candidato agcProvaCandidato) {
        this.agcProvaCandidato = agcProvaCandidato;
    }

    public String getResultado() {
        return resultado;
    }

    public void setResultado(String resultado) {
        this.resultado = resultado;
    }
}
